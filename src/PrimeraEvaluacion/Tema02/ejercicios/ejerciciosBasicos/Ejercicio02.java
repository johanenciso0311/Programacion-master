package PrimeraEvaluacion.Tema02.ejercicios.ejerciciosBasicos;

/*
Escribe un programa que imprima las siguientes tres líneas:
primera
segunda
tercera
 */
public class Ejercicio02 {
    public static void main(String[] args) {
        System.out.println("primera");
        System.out.println("segunda");
        System.out.println("tercera");

        System.out.println();
        System.out.println("Otra forma:");
        System.out.println("primera\nsegunda\ntercera"); // para hacer un salto de línea dentro de una cadena de texto: \n
    }
}
